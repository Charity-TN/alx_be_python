# Calculate the simple interest using the following variables
P = 1000 #principal (representing $1000)
R = 0.05 #rate  (representing 5% annual interest rate)
T = 3    #time (representing 3 years)

#Print these variables
print(f"P (Principal): {P}")
print(f"R (Rate): {R}")
print(f"T (Time): {T}")

# Calculate the simple interest
I = P * R * T

#Print the results
print(f"The simple interest is : {I}")
